import 'package:flutter/cupertino.dart';

class RequestFocusWidget extends StatelessWidget {
  const RequestFocusWidget({
    super.key,
    required this.child,
  });

  final Widget child;

  @override
  Widget build(BuildContext context) {
    return GestureDetector(
      onTap: () {
        FocusScope.of(context).requestFocus(FocusNode());
      },
      child: child,
    );
  }
}
